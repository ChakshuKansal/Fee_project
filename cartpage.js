function addToCart(index) {
  const product = allmenaccess.find(p => p.id === index);
  if (product) {
      const existingItemIndex = cart.findIndex(item => item.id === index);
      if (existingItemIndex !== -1) {
          cart[existingItemIndex].quantity++;
      } else {
          cart.push({ ...product, quantity: 1 });
      }
      displayCart();
      saveCartToLocalStorage(); // Save cart data to localStorage
  }

}


if(currentIndex==existingindex){
  addToCart(currentIndex);
  
}


function displayCart() {
  const cartItemsContainer = document.getElementById('cartproducts');
  const cartTotalElement = document.getElementById('cart-total');
  cartItemsContainer.innerHTML = '';
  let total = 0;
  cart.forEach(item => {
      const listItem = document.createElement('div');
      listItem.innerHTML = `<div id="card">
      <div id="details">
      <div class="imageholder" style="background-image:url('${item.image}')"></div>
      <div>${item.name} - ${item.price} x ${item.quantity}</div>
      </div>
      <button id="increase" onclick="increaseQuantity(${item.id})">+</button>
      <button id="remove" onclick="removeFromCart(${item.id})">Remove</button>
      <button id="decrease" onclick="decreaseQuantity(${item.id})">-</button>
      </div>`;

      cartItemsContainer.appendChild(listItem);
      const priceMatch = item.price.match(/\d+/); // Extracts the digits from the string
        const itemPrice = priceMatch ? parseInt(priceMatch[0]) : 0; // Parse the price as an integer
      total += itemPrice * item.quantity;
      console.log(total);
  });
  cartTotalElement.textContent = `Total: ₹${total.toFixed(2)}`;
}
